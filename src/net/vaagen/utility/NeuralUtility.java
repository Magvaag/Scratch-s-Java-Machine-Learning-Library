public class NeuralUtility {

    public static int[] sortOutput(float[] floats) {
        // 0 is highest value, length-1 is lowest
        int[] sortedList = new int[floats.length];



        return sortedList;
    }

    public static float[] intArrayToFloat(int[] i) {
        float[] f = new float[i.length];
        for (int a = 0; a < i.length; a++) {
            f[a] = i[a];
        }
        return f;
    }

}
